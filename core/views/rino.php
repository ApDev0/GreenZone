                        <h5>
                            Os rinocerontes fazem parte da ordem Perissodactyla (mamíferos de dedos ímpares) e da família Rhinocerotidae.
                            Estão divididos em 4 gêneros e 5 espécies:
                            Ceratotherium simum, Dicerorhinus sumatrensis, Diceros bicornis, Rhinoceros sondaicus e Rhinoceros unicornis.
