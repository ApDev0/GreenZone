                        <p></p>
                        <h3>
                            Características
                        </h3>
                        <h5>
                            Eles habitam desde savanas a florestas mais densas.
                            São animais que possuem um ou dois cornos, estrutura de origem dérmica,
                            composta por pelos queratinosos compactados. Possuem três dedos e uma pele grossa cheia de pregas,
                            os dentes caninos e incisivos são vestigiais, exceto nos asiáticos. As fêmeas reproduzem a cada 2 anos,
                            a gestação dura de 420 a 570 dias e seu único filhote permanece com ela até sua próxima gestação. <br>
                            <p></p>
                            Elas atingem a maturidade sexual entre 4 e 6 anos e os machos entre 7 e 10 anos e a expectativa de vida pode chegar aos 50 anos.
                            São animais herbívoros, na África alimentam-se mais de pastagem, enquanto na Ásia buscam folhas nas árvores.
                            Possuem hábitos mais noturnos, descansando ao longo do dia. <br>
                            Apesar de pesados podem atingir grandes velocidades em pequenas distâncias e acabam abrindo trilhas por onde passam.
                            Gostam de tomar banho de lama, usada como repelente de insetos e os machos são solitários e territorialistas,
                            marcando seu território com urina e uma pilha de fezes. Durante a estadia reprodutiva os casais podem permanecer até 4 meses juntos.
                        </h5>
