                    <h3>Rinoceronte Branco (Ceratotherium simum)</h3>
                    <h5>
                        A maior das espécies é dividida em duas subespécies:
                        o rinoceronte branco do norte encontra-se em menos de uma dezena no Congo e em cativeiro,
                        sendo inviável a reprodução natural entre eles. Há um projeto de transformar células tronco pluripotentes em gametas,
                        para uma reprodução em laboratório.
                        O Rinoceronte Branco do sul está por Botswana, Namíbia, Swazilândia, Zimbabue, Costa do Marfim, Quenia e Zambia.
                        Os governos africanos tem mantido esta espécie em Parques Nacionais.
                        Pesam até 4000 kg e alcançam até 3,77 metros de comprimento, sendo o segundo maior mamífero terrestre.
